 ## How accurate should π be?
 We commonly remember π as 3.14. However, in technological fields, it should be more accurate than that. According to NASA Jet Propulsion Laboratory's official blog, NASA uses the value of 3.141592653589793. This is π cut off at the **15th decimal point**. There are a few reasons for this.
- - -
1. On Earth, this approximation only makes an error of distance 10,000 times thinner than a hair when calculating Earth's circumference.
1. In our solar system, this approximation only makes an error of distance 1.5 inches when calculating the circumference of the 25 billion mile diameter circle (The most distant spacecraft Voyager 1 just went half of this distance.)
